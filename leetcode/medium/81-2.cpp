template<typename T>
ostream& operator <<(ostream &s, const vector<T> &c) {
s<<"[ ";
for (auto it : c) s << it << " ";
s<<"]";
return s;
}

class Solution {
public:
    bool search(vector<int>& nums, int target) {
      if(nums.empty()) return false;
      return sub_search(nums, 0, nums.size()-1, target);
    }
    
private:
    bool sub_search(vector<int>& nums, int lb ,int rb, int target){
      if(lb >= rb) return nums[lb] == target;

      int mid = (lb + rb) / 2;
      int ret;

      if(nums[mid] == target) return true;

      if(nums[lb] < nums[mid]) ret = binary_search(nums, lb, mid - 1, target);
      else ret = sub_search(nums, lb, mid - 1, target);

      if(ret) return ret;

      if(nums[rb] > nums[mid]) ret = binary_search(nums, mid + 1, rb, target);
      else ret = sub_search(nums, mid + 1, rb, target);

      return ret;

    }
    bool binary_search(vector<int>& nums, int lb, int rb, int target){
      int mid;

      while(lb < rb){
        mid = (lb + rb ) / 2;
        if(nums[mid] == target) return true;
        else if(nums[mid] < target) lb = mid + 1;
        else rb = mid - 1;
      }

      return nums[lb] == target;
    }
};

